            <p>
                La balise h1 est une balise présente dans le code d’une page web pour marquer le titre principal du
                contenu
                (par exemple, le titre d’un article de blog, le titre principal du texte de la page…). A ne pas
                confondre
                avec
                la balise title.
                Les balises h2, h3… servent à marquer les « sous-titres » (h2), les « sous-sous-titres » (h3)…
                Une seule balise h1 est prise en compte sur chaque page (il ne peut y avoir qu’un seul « grand titre »
                à un
                article de blog). En revanche, il peut y avoir plusieurs sous-titres (donc plusieurs balises h2, h3,
                h4…
                jusqu’à h6).
                Ces balises sont généralement placées sur des mots (le titre et les sous-titres du texte de la page).
                Mais
                elles peuvent aussi être placées sur une image. On voit parfois des balises h1 placées sur le logo (ce
                qui
                est
                une erreur pour le référencement).
            </p>
